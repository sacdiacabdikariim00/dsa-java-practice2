public class while_loop {
    public static void main(String[] args) {

//        syntax of while loop
//        while (CONDITION) {
//            //body
//        }

        int num=1;
        while (num<=5){
            System.out.println(num);
            num +=1;
        }



        int count=1;
        while(count!=5){
            System.out.println(count);
            count++;
        }
    }
}
